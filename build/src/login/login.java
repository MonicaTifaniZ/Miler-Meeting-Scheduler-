    private void loginsistem() throws SQLException {
        String user = txtUsername.getText();
        String pass = String.valueOf(txtPassword.getPassword());
        if (user.trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Username tidak boleh kosong!");
        } else if (pass.trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Password tidak boleh kosong!");
        } else {
            try {
                Connection conn = koneksi.getConnection();
                String sql = "SELECT * FROM user WHERE username='" + user + "'and password = '" + pass + "'";
                Statement s = conn.createStatement();
                ResultSet r = s.executeQuery(sql);
                boolean ada = r.next();

                if (!ada) {
                    JOptionPane.showMessageDialog(null, "Username atau password anda salah!!!");
                } else {
                    String s_cek = r.getString("status_user");
                    if (s_cek.equalsIgnoreCase("admin")) {
                        this.setVisible(false);
                        frameAdmin admin = new frameAdmin();
                        admin.setVisible(true);
                    } else if(s_cek.equalsIgnoreCase("anggota")){
                        this.setVisible(false);
                        frameAnggota anggota = new frameAnggota();
                        anggota.setVisible(true);
                    } else if (s_cek.equalsIgnoreCase("ketua")) {
                        this.setVisible(false);
                        frameLihatJadwal lihatJadwal = new frameLihatJadwal();
                        lihatJadwal.setVisible(true);
                    }
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, " Bye error : " + e.getMessage());
            }

        }

    }

